function bundle = build_results_bundle_v9(results, varargin)
%BUILD_RESULTS_BUNDLE_V9 Assemble a standard app-facing results bundle.

if ~isstruct(results) || ~isfield(results, 'summary') || ~istable(results.summary)
    error('Input must be a v9 results struct containing results.summary.');
end

p = inputParser;
p.addParameter('validationReport', [], @(x) isempty(x) || isstruct(x));
p.addParameter('targetedVsRandom', [], @(x) isempty(x) || istable(x) || isstruct(x));
p.addParameter('doNothing', [], @(x) isempty(x) || isstruct(x));
p.addParameter('attributableRisk', [], @(x) isempty(x) || isstruct(x));
p.addParameter('charts', [], @(x) isempty(x) || isstruct(x));
p.addParameter('downloads', [], @(x) isempty(x) || isstruct(x));
p.parse(varargin{:});
opt = p.Results;

summary = summarise_results_v9(results);

bundle = struct();

bundle.metadata = struct( ...
    'available', true, ...
    'source', 'results.interfaceConfig', ...
    'contractVersion', 'apy_v9_results_bundle_v1', ...
    'modelVersion', get_if_present(results, {'interfaceConfig', 'modelVersion'}), ...
    'configVersion', get_if_present(results, {'interfaceConfig', 'configVersion'}), ...
    'scenarioLabel', get_if_present(results, {'interfaceConfig', 'scenarioLabel'}), ...
    'usesDefaults', get_if_present(results, {'interfaceConfig', 'usesDefaults'}), ...
    'sourceDataFiles', get_if_present(results, {'interfaceConfig', 'sourceDataFiles'}));

bundle.validation = struct( ...
    'available', ~isempty(opt.validationReport), ...
    'source', iff(~isempty(opt.validationReport), 'collect_validation_issues_v9', ''), ...
    'report', opt.validationReport, ...
    'reportRows', struct_array_or_empty(opt.validationReport));

bundle.headline = struct( ...
    'available', true, ...
    'source', 'results.summary + summarise_results_v9', ...
    'strategy', summary.strategy, ...
    'calibration', summary.calibration, ...
    'keyMetricsTable', summary.keyMetrics, ...
    'keyMetricsRows', table_rows_or_empty(summary.keyMetrics), ...
    'summaryTable', summary.summaryTable, ...
    'summaryRows', table_rows_or_empty(summary.summaryTable));

bundle.technical = struct( ...
    'available', true, ...
    'source', 'run_scenario_v9 / tb_screening_mc_model_v9', ...
    'calibration', get_if_present(results, {'calibration'}), ...
    'exampleCohortMeta', table_meta_or_empty(get_if_present(results, {'exampleCohort'})), ...
    'rawMeta', table_meta_or_empty(get_if_present(results, {'raw'})), ...
    'interfaceConfig', get_if_present(results, {'interfaceConfig'}));

bundle.targetedVsRandom = struct( ...
    'available', ~isempty(opt.targetedVsRandom), ...
    'source', iff(~isempty(opt.targetedVsRandom), 'optional add-on input', ''), ...
    'comparisonTable', table_or_empty(opt.targetedVsRandom), ...
    'comparisonRows', table_rows_or_empty(opt.targetedVsRandom), ...
    'payload', struct_payload_or_empty(opt.targetedVsRandom));

bundle.doNothing = struct( ...
    'available', ~isempty(opt.doNothing), ...
    'source', iff(~isempty(opt.doNothing), 'run_tb_screening_do_nothing_v9', ''), ...
    'summaryTable', get_if_present(opt.doNothing, {'summary'}), ...
    'summaryRows', table_rows_or_empty(get_if_present(opt.doNothing, {'summary'})), ...
    'derivedTable', get_if_present(opt.doNothing, {'derived'}), ...
    'derivedRows', table_rows_or_empty(get_if_present(opt.doNothing, {'derived'})), ...
    'resultsUsed', get_if_present(opt.doNothing, {'resultsUsed'}));

bundle.attributableRisk = struct( ...
    'available', ~isempty(opt.attributableRisk), ...
    'source', iff(~isempty(opt.attributableRisk), 'run_tb_screening_reactivation_attributable_v9', ''), ...
    'summaryTable', get_if_present(opt.attributableRisk, {'summary'}), ...
    'summaryRows', table_rows_or_empty(get_if_present(opt.attributableRisk, {'summary'})), ...
    'attributableTable', get_if_present(opt.attributableRisk, {'attributableTable'}), ...
    'attributableRows', table_rows_or_empty(get_if_present(opt.attributableRisk, {'attributableTable'})), ...
    'naturalHistoryResults', get_if_present(opt.attributableRisk, {'naturalHistoryResults'}), ...
    'figureFiles', get_if_present(opt.attributableRisk, {'figureFiles'}));

bundle.charts = struct( ...
    'available', ~isempty(opt.charts), ...
    'source', iff(~isempty(opt.charts), 'run_tb_screening_igra_charts_v9', ''), ...
    'plotDataTable', get_if_present(opt.charts, {'plotData'}), ...
    'plotDataRows', table_rows_or_empty(get_if_present(opt.charts, {'plotData'})), ...
    'dataCsv', get_if_present(opt.charts, {'dataCsv'}), ...
    'figureFiles', get_if_present(opt.charts, {'figureFiles'}));

bundle.downloads = struct( ...
    'available', ~isempty(opt.downloads), ...
    'source', iff(~isempty(opt.downloads), 'export_results_v9', ''), ...
    'summaryCsv', get_if_present(opt.downloads, {'summaryCsv'}), ...
    'keyMetricsCsv', get_if_present(opt.downloads, {'keyMetricsCsv'}), ...
    'payload', opt.downloads);
end

function value = get_if_present(s, pathParts)
value = [];
if isempty(s) || ~isstruct(s)
    return;
end
value = s;
for i = 1:numel(pathParts)
    name = pathParts{i};
    if ~isstruct(value) || ~isfield(value, name)
        value = [];
        return;
    end
    value = value.(name);
end
end

function out = table_rows_or_empty(value)
if istable(value)
    out = table2struct(value, 'ToScalar', false);
else
    out = struct.empty(0, 1);
end
end

function out = table_meta_or_empty(value)
if istable(value)
    out = struct( ...
        'rowCount', height(value), ...
        'variableNames', {value.Properties.VariableNames});
else
    out = struct();
end
end

function out = table_or_empty(value)
if istable(value)
    out = value;
else
    out = [];
end
end

function out = struct_payload_or_empty(value)
if isstruct(value)
    out = value;
else
    out = struct();
end
end

function out = struct_array_or_empty(value)
if isempty(value)
    out = struct.empty(0, 1);
elseif isstruct(value)
    out = value;
else
    out = struct.empty(0, 1);
end
end

function out = iff(cond, a, b)
if cond
    out = a;
else
    out = b;
end
end
